import React, { useState, useEffect } from "react";
import { maintenanceApi } from "../../../shared/api/apiClient";
import TenantLayout from "../../../shared/layouts/TenantLayout";
import "../styles/tenant-common.css";

const MaintenancePage = () => {
  const [loading, setLoading] = useState(true);
  const [showForm, setShowForm] = useState(false);
  const [requests, setRequests] = useState([]);
  const [formData, setFormData] = useState({
    category: "other",
    title: "",
    description: "",
  });

  useEffect(() => {
    loadRequests();
  }, []);

  const loadRequests = async () => {
    try {
      setLoading(true);
      const data = await maintenanceApi.getMyRequests(50);
      setRequests(data.requests || []);
    } catch (error) {
      console.error("Failed to load maintenance requests:", error);
    } finally {
      setLoading(false);
    }
  };

  const handleSubmitRequest = async (e) => {
    e.preventDefault();
    try {
      await maintenanceApi.createRequest(formData);
      setFormData({ category: "other", title: "", description: "" });
      setShowForm(false);
      await loadRequests();
    } catch (error) {
      console.error("Failed to submit maintenance request:", error);
    }
  };

  const getStatusClass = (status) => {
    return status === "Completed"
      ? "badge-success"
      : status === "In Progress"
        ? "badge-info"
        : "badge-warning";
  };

  return (
    <TenantLayout>
      <div className="tenant-page">
        <div className="page-header">
          <div>
            <h1>
              <i className="fas fa-tools"></i> Maintenance Requests
            </h1>
            <p>Submit and track your maintenance requests</p>
          </div>
          <button
            className="btn btn-primary"
            onClick={() => setShowForm(!showForm)}
          >
            <i className="fas fa-plus"></i> New Request
          </button>
        </div>

        {showForm && (
          <div className="section-card">
            <h2>Submit Maintenance Request</h2>
            <form className="maintenance-form" onSubmit={handleSubmitRequest}>
              <div className="form-group">
                <label>Category</label>
                <select
                  className="form-control"
                  value={formData.category}
                  onChange={(e) =>
                    setFormData({ ...formData, category: e.target.value })
                  }
                  required
                >
                  <option value="plumbing">Plumbing</option>
                  <option value="electrical">Electrical</option>
                  <option value="hardware">Hardware</option>
                  <option value="appliance">Appliance</option>
                  <option value="cleaning">Cleaning</option>
                  <option value="other">Other</option>
                </select>
              </div>
              <div className="form-group">
                <label>Title</label>
                <input
                  type="text"
                  className="form-control"
                  placeholder="Brief description"
                  value={formData.title}
                  onChange={(e) =>
                    setFormData({ ...formData, title: e.target.value })
                  }
                  required
                />
              </div>
              <div className="form-group">
                <label>Description</label>
                <textarea
                  className="form-control"
                  rows="4"
                  placeholder="Detailed description of the issue"
                  value={formData.description}
                  onChange={(e) =>
                    setFormData({ ...formData, description: e.target.value })
                  }
                  required
                ></textarea>
              </div>
              <div className="form-actions">
                <button
                  type="button"
                  className="btn btn-secondary"
                  onClick={() => setShowForm(false)}
                >
                  Cancel
                </button>
                <button type="submit" className="btn btn-primary">
                  Submit Request
                </button>
              </div>
            </form>
          </div>
        )}

        <div className="section-card">
          <h2>Request History</h2>
          <div className="maintenance-list">
            {requests.map((request) => (
              <div key={request.id} className="maintenance-item">
                <div className="maintenance-info">
                  <h3>{request.title}</h3>
                  <p>
                    {request.category} â€¢{" "}
                    {new Date(request.date).toLocaleDateString()}
                  </p>
                </div>
                <span className={`badge ${getStatusClass(request.status)}`}>
                  {request.status}
                </span>
              </div>
            ))}
          </div>
        </div>
      </div>
    </TenantLayout>
  );
};

export default MaintenancePage;
